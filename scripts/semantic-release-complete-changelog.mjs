import { writeFile } from "node:fs/promises";

const GITHUB_API = "https://api.github.com";

export async function prepare(pluginConfig, context) {
  const changelogFile = pluginConfig.changelogFile ?? "CHANGELOG.md";
  const repository = getRepository(context);
  const token = context.env.GITHUB_TOKEN ?? context.env.GH_TOKEN;

  if (!token) {
    throw new Error("GITHUB_TOKEN or GH_TOKEN is required to generate the complete changelog.");
  }

  const releases = await fetchReleases(repository, token);
  const previousEntries = releases
    .filter((release) => !release.draft && release.tag_name !== context.nextRelease.gitTag)
    .map((release) => releaseEntry(repository, release));

  const currentEntry = currentReleaseEntry(repository, context.nextRelease);
  const entries = [currentEntry, ...previousEntries];
  const changelog = [
    "# Changelog",
    "",
    "All notable changes to this project are generated by semantic-release from GitHub release history.",
    "",
    entries.join("\n\n"),
  ].join("\n").trimEnd() + "\n";

  await writeFile(changelogFile, changelog, "utf8");
  context.logger.log("Generated complete changelog with %d release entries", previousEntries.length + 1);
}

function getRepository(context) {
  if (context.env.GITHUB_REPOSITORY) return context.env.GITHUB_REPOSITORY;

  const repositoryUrl = context.options.repositoryUrl ?? "";
  const match = repositoryUrl.match(/github\.com[:/](?<owner>[^/]+)\/(?<repo>[^/.]+)(?:\.git)?/i);
  if (!match?.groups) {
    throw new Error("Unable to determine GitHub repository for changelog generation.");
  }

  return `${match.groups.owner}/${match.groups.repo}`;
}

async function fetchReleases(repository, token) {
  const releases = [];

  for (let page = 1; ; page += 1) {
    const response = await fetch(`${GITHUB_API}/repos/${repository}/releases?per_page=100&page=${page}`, {
      headers: {
        Accept: "application/vnd.github+json",
        Authorization: `Bearer ${token}`,
        "User-Agent": "meibo-semantic-release",
        "X-GitHub-Api-Version": "2022-11-28",
      },
    });

    if (!response.ok) {
      throw new Error(`Failed to fetch GitHub releases: ${response.status} ${response.statusText}`);
    }

    const pageReleases = await response.json();
    releases.push(...pageReleases);

    if (pageReleases.length < 100) break;
  }

  return releases.sort((left, right) => {
    const leftDate = new Date(left.published_at ?? left.created_at ?? 0).getTime();
    const rightDate = new Date(right.published_at ?? right.created_at ?? 0).getTime();
    return rightDate - leftDate;
  });
}

function currentReleaseEntry(repository, nextRelease) {
  const url = releaseUrl(repository, nextRelease.gitTag);
  const date = new Date().toISOString().slice(0, 10);
  const notes = normalizeNotes(nextRelease.notes, nextRelease.gitTag, nextRelease.version);

  return [`## [${nextRelease.version}](${url}) (${date})`, "", notes].join("\n").trimEnd();
}

function releaseEntry(repository, release) {
  const tag = release.tag_name;
  const version = tag.replace(/^v/, "");
  const date = (release.published_at ?? release.created_at ?? "").slice(0, 10);
  const url = release.html_url ?? releaseUrl(repository, tag);
  const notes = normalizeNotes(release.body, tag, version);

  return [`## [${version}](${url}) (${date})`, "", notes].join("\n").trimEnd();
}

function releaseUrl(repository, tag) {
  return `https://github.com/${repository}/releases/tag/${encodeURIComponent(tag)}`;
}

function normalizeNotes(notes, tag, version) {
  const cleaned = (notes ?? "").trim();
  if (!cleaned) return "_No release notes provided._";

  const headingPattern = new RegExp(
    `^#{1,2}\\s+.*(?:${escapeRegExp(tag)}|${escapeRegExp(version)}).*?(?:\\r?\\n){2,}`,
    "i",
  );

  return cleaned.replace(headingPattern, "").trim() || "_No release notes provided._";
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}